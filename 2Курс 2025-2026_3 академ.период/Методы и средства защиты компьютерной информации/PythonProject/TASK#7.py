# Циклы в Python

# В Python циклы позволяют повторять набор инструкций до выполнения определённых условий.
# Один из самых популярных циклов — цикл for.

# Цикл for используется для итерации по последовательностям: списки, строки, диапазоны и др.

print("Пример цикла по списку:")
for элемент in [1, 2, 3, 4]:
    print(элемент)

# Функция range() создаёт диапазон чисел и часто используется в циклах for.

print("\nПример range(конец):")
for i in range(5):  # от 0 до 4
    print(i)

print("\nПример range(начало, конец, шаг):")
for i in range(1, 10, 2):  # от 1 до 9 с шагом 2
    print(i)

# Обратный цикл — range() с отрицательным шагом

print("\nОбратный цикл от 10 до 1:")
for i in range(10, 0, -1):
    print(i)

# Настройка функции print(): параметр end

print("\nВывод чисел на одной строке:")
for i in range(5):
    print(i, end=' ')  # выводит: 0 1 2 3 4

print("\n")  # переход на новую строку

# Пример задачи с циклом

print("Задача 1: Вывести числа от 1 до 10")
for i in range(1, 11):
    print(i)

print("Задача 2: Вывести числа от 10 до 1")
for i in range(10, 0, -1):
    print(i)
