# Циклы в Python

# Цикл for позволяет повторять действия для каждого элемента в последовательности.

# Пример цикла по списку:
for элемент in [1, 2, 3, 4]:
    print(элемент)

# Функция range() создаёт диапазон чисел.
# Пример 1: range(конец)
for i in range(5):  # от 0 до 4
    print(i)

# Пример 2: range(начало, конец, шаг)
for i in range(1, 10, 2):  # от 1 до 9 с шагом 2
    print(i)

# Обратный цикл: range() с отрицательным шагом
for i in range(10, 0, -1):  # от 10 до 1
    print(i)

# Настройка функции print(): параметр end
for i in range(5):
    print(i, end=' ')  # выводит: 0 1 2 3 4

print()  # переход на новую строку

