# Тема 10: Цикл while

# 1. Синтаксис цикла while
# Цикл while выполняет действия, пока условие истинно.

# Пример синтаксиса:
# while условие:
#     # Тело цикла
#     # Действия для выполнения

# Условие проверяется перед каждой итерацией.
# Если оно ложно — цикл завершится.

# 2. Инструкции управления циклом
# break — прерывает выполнение цикла досрочно
# continue — пропускает текущую итерацию и переходит к следующей

# -------------------------------
# Пример 21: Программа угадывания числа
# -------------------------------

import random

# Компьютер загадывает число от 1 до 100
a = random.randint(1, 100)
b = int(input("Угадайте загаданное число: "))
k = 1  # Счётчик попыток

if b == a:
    print('Вы угадали число с', k, 'попытки!')
else:
    while b != a:
        k += 1
        if b > a:
            b = int(input("Введённое число больше. Попробуйте снова! "))
        elif b < a:
            b = int(input("Введённое число меньше. Попробуйте снова! "))
    print('Вы угадали число с', k, 'попытки!')

# -------------------------------
# Пример 22: Проверка знаний таблицы умножения
# -------------------------------

from random import randint

k = 0  # Количество правильных ответов
n = 0  # Количество заданных примеров

while n != 5:
    a = randint(1, 10)
    b = randint(1, 10)
    print(a, "*", b, "=?")
    c = int(input())
    if c == a * b:
        k += 1
    n += 1

print('Количество правильных ответов =', k)
