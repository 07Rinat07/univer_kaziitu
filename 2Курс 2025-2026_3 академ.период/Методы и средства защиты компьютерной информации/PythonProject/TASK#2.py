# -----------------------------------------------------
# 1. СИНТАКСИС УСЛОВНОГО ОПЕРАТОРА
# -----------------------------------------------------

print("=== 1. СИНТАКСИС УСЛОВНОГО ОПЕРАТОРА ===")

# Ввод числа пользователем
x = int(input("Введите число: "))

# Проверка условия — является ли число положительным
if x > 0:
    print(f"{x} - положительное число")
else:
    print(f"{x} - отрицательное число")

print()  # Пустая строка для разделения вывода


# -----------------------------------------------------
# 2. ВЛОЖЕННЫЕ УСЛОВНЫЕ ИНСТРУКЦИИ
# -----------------------------------------------------

print("=== 2. ВЛОЖЕННЫЕ УСЛОВНЫЕ ИНСТРУКЦИИ ===")

x = int(input("Введите число снова: "))

# Проверяем, положительное ли число, ноль или отрицательное
if x > 0:
    print(f"{x} - положительное число")
else:
    # Вложенный if: дополнительная проверка
    if x == 0:
        print(f"{x} - это ноль")
    else:
        print(f"{x} - отрицательное число")

print()


# -----------------------------------------------------
# 3. ОПЕРАТОРЫ СРАВНЕНИЯ
# -----------------------------------------------------

print("=== 3. ОПЕРАТОРЫ СРАВНЕНИЯ ===")

a = 10
b = 20

# Проверка равенства
if a == b:
    print("a равно b")
else:
    print("a не равно b")

# Демонстрация всех операторов сравнения
print("a < b:", a < b)     # True
print("a > b:", a > b)     # False
print("a != b:", a != b)   # True
print("a <= b:", a <= b)   # True
print("a >= b:", a >= b)   # False

print()


# -----------------------------------------------------
# 4. ТИПЫ ДАННЫХ В УСЛОВИЯХ
# -----------------------------------------------------

print("=== 4. ТИПЫ ДАННЫХ В УСЛОВИЯХ ===")

# Пример со строкой
name = input("Введите имя: ")

if name == "Али":
    print("Привет, Али!")
else:
    print("Привет, незнакомец!")

# Пример с булевым значением
is_active = True  # Логическое значение (True или False)

if is_active:
    print("Пользователь активен.")
else:
    print("Пользователь неактивен.")

print()


# -----------------------------------------------------
# 5. ЛОГИЧЕСКИЕ ОПЕРАТОРЫ
# -----------------------------------------------------

print("=== 5. ЛОГИЧЕСКИЕ ОПЕРАТОРЫ ===")

age = int(input("Введите ваш возраст: "))

# Пример с оператором AND
if age > 18 and age < 60:
    print("Вы трудоспособного возраста")
else:
    print("Вы не трудоспособного возраста")

# Пример с оператором OR
if age < 18 or age > 60:
    print("Вы не входите в трудоспособный возраст")

# Пример с оператором NOT
is_student = False

if not is_student:
    print("Вы не студент.")
else:
    print("Вы студент.")

print()


# -----------------------------------------------------
# 6. КАСКАДНЫЕ УСЛОВНЫЕ ИНСТРУКЦИИ (ELIF)
# -----------------------------------------------------

print("=== 6. КАСКАДНЫЕ УСЛОВНЫЕ ИНСТРУКЦИИ ===")

score = int(input("Введите вашу оценку: "))

# Используем каскадные проверки
if score >= 90:
    print("Отлично!")
elif score >= 70:
    print("Хорошо!")
elif score >= 50:
    print("Удовлетворительно")
else:
    print("Неудовлетворительно")

print()
print("=== Программа завершена ===")
