# Тема 8-9: Работа со строками

# Строки — это последовательности символов. С ними можно выполнять множество операций.

# Ввод строки с клавиатуры
s = input("Введите строку: ")

# Конкатенация строк (соединение)
s1 = "Привет"
s2 = "Мир"
s3 = s1 + " " + s2
print("Конкатенация:", s3)  # Привет Мир

# Повторение строки
s = "Привет"
print("Повторение строки:", s * 3)  # ПриветПриветПривет

# Длина строки
s = "Python"
print("Длина строки:", len(s))  # 6

# Преобразование числа в строку
num = 123
str_num = str(num)
print("Преобразованное число:", str_num)  # '123'

# Методы строк

# find(): поиск слева направо
s = "Программирование на Python"
index_find = s.find("программа")
print("Индекс подстроки (find):", index_find)  # 14

# rfind(): поиск справа налево
index_rfind = s.rfind("а")
print("Индекс последнего 'а' (rfind):", index_rfind)  # 25

# replace(): замена подстроки
s = "Привет, мир!"
new_s = s.replace("мир", "Python")
print("После замены:", new_s)  # Привет, Python!

# count(): количество вхождений
s = "программа"
count_a = s.count("а")
print("Количество 'а':", count_a)  # 3

