# 1. Целая арифметика
a = 10
b = 3
print("Сложение:", a + b)       # 10 + 3 = 13
print("Вычитание:", a - b)      # 10 - 3 = 7
print("Умножение:", a * b)      # 10 * 3 = 30
print("Возведение в степень:", a ** b)  # 10^3 = 1000
print("Деление:", a / b)        # 10 / 3 = 3.333...
print("Целочисленное деление:", a // b)  # 10 // 3 = 3
print("Остаток от деления:", a % b)     # 10 % 3 = 1


